#include<iostream>
using namespace std;

int main()
{ 
    int a, b, c;

    cout << "Enter the value of a: ";
    cin >> a;

    cout << "Enter the value of b: ";
    cin >> b;

    c = a + b;

    cout << "Value of a: " << a << " + " << "Value of b: " << b << " = " << "Value of c: " << c << endl;

    return 0;
}